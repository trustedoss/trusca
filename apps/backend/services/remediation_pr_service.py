class ProjectNotOptedIn(RemediationError):
    """409 — the project has no opted-in, usable GitHub App installation.

    A credential alone does NOT authorise a write: a team must explicitly link an
    installation (with a repository) to the project first. Surfaced as 409 (a
    precondition the caller can satisfy by opting in) rather than 404 — the
    project itself is visible to the caller (team_admin), only the opt-in is
    missing.
    """

    status_code = 409
    title = "Project Not Opted In"
    type_uri = "https://docs.trustedoss.io/errors/remediation-not-opted-in"


class RemediationForbidden(RemediationError):
    """403 — the caller is a project member but not a team_admin."""

    status_code = 403
    title = "Forbidden"
    type_uri = "https://docs.trustedoss.io/errors/remediation-forbidden"


class RemediationConfigError(RemediationError):
    """422 — a stored value is unusable (e.g. malformed repository_full_name)."""

    status_code = 422
    title = "Remediation Configuration Unusable"
    type_uri = "https://docs.trustedoss.io/errors/remediation-config"


class GitHubWriteError(RemediationError):
    """502 — a GitHub write (branch / commit / PR) failed.

    The message carries the failing STEP + STATUS code only — never the GitHub
    response body or any token material.
    """

    status_code = 502
    title = "GitHub Write Failed"
    type_uri = "https://docs.trustedoss.io/errors/remediation-github-write"


