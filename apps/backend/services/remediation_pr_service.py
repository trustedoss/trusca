@dataclass(frozen=True)
class _OptIn:
    """The resolved opt-in context the write derives everything from."""

    installation_row_id: uuid.UUID
    installation_id: str  # GitHub's numeric installation id (string)
    credential_id: uuid.UUID
    repository_full_name: str


async def _resolve_project_team_admin(
    session: AsyncSession,
    *,
    project_id: uuid.UUID,
    actor: CurrentUser,
) -> Project:
    """Load the project, enforcing existence-hide + team_admin RBAC.

    A caller who cannot see the project gets 404 (existence-hide, no cross-team
    enumeration). A member who is not team_admin gets 403.
    """
    project = (
        await session.execute(select(Project).where(Project.id == project_id))
    ).scalar_one_or_none()
    if project is None or not _is_team_member(actor, project.team_id):
        raise ProjectNotAccessible(f"project {project_id} not found")
    if not _is_team_admin(actor, project.team_id):
        raise RemediationForbidden(
            "opening a remediation PR requires team_admin of the project's team"
        )
    return project


async def _resolve_opt_in(
    session: AsyncSession,
    *,
    project_id: uuid.UUID,
) -> _OptIn:
    """Resolve the project's opted-in, usable installation, else 409.

    Usable means: an installation linked to THIS project, whose credential is
    NOT revoked, and which carries a ``repository_full_name``. The target repo +
    token are derived from this row ONLY — there is no caller-supplied repo.
    """
    row = (
        await session.execute(
            select(GitHubAppInstallation, GitHubAppCredential)
            .join(
                GitHubAppCredential,
                GitHubAppCredential.id == GitHubAppInstallation.credential_id,
            )
            .where(GitHubAppInstallation.project_id == project_id)
            .where(GitHubAppInstallation.repository_full_name.is_not(None))
            .where(GitHubAppCredential.revoked_at.is_(None))
            .order_by(GitHubAppInstallation.created_at.desc())
        )
    ).first()
    if row is None:
        raise ProjectNotOptedIn(
            "project is not opted in to automated remediation PRs: link a GitHub "
            "App installation (with a repository) to this project first"
        )
    installation, credential = row
    # repository_full_name is guaranteed non-NULL by the WHERE clause above.
    assert installation.repository_full_name is not None  # noqa: S101 — invariant
    return _OptIn(
        installation_row_id=installation.id,
        installation_id=installation.installation_id,
        credential_id=credential.id,
        repository_full_name=installation.repository_full_name,
    )


