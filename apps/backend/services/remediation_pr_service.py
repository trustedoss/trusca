async def create_npm_remediation_pr(
    session: AsyncSession,
    actor: CurrentUser,
    project_id: uuid.UUID,
    *,
    manifest_override: str | None = None,
    http_client: httpx.AsyncClient | None = None,
) -> RemediationPRResult:
    """Open (or return the existing) automated npm remediation PR for a project.

    Steps (each gated / defensive — see the module docstring):
      1. RBAC: team_admin of the project's team (existence-hide on no access).
      2. Resolve the project's OPT-IN installation (target repo derived here,
         NEVER from the caller) — 409 if not opted in.
      3. Compute the b2 dry-run (the single source of the proposed edit). No
         changes → no-op result (do not open an empty PR).
      4. Idempotency: an existing ``open`` PR with the same fingerprint is
         returned (no duplicate).
      5. Persist a ``creating`` row, mint the installation token, perform the
         GitHub writes (branch → commit → PR) with ``follow_redirects=False``,
         flip the row to ``open`` (or ``failed``).
    """
    project = await _resolve_project_team_admin(
        session, project_id=project_id, actor=actor
    )
    opt_in = await _resolve_opt_in(session, project_id=project_id)

    # b2 is the single source of the proposed edit — never recompute it here.
    dry_run = await compute_npm_dry_run(
        session, actor, project_id, manifest_override=manifest_override
    )
    if not dry_run.changed or dry_run.edited_manifest is None:
        log.info(
            "remediation_pr.no_op",
            project_id=str(project_id),
            reason="no_manifest_change",
        )
        return RemediationPRResult(
            record=None, created=False, no_op_reason="no_manifest_change"
        )

    bumps = _applied_bumps(dry_run)
    if not bumps:
        return RemediationPRResult(
            record=None, created=False, no_op_reason="no_applied_bumps"
        )
    fingerprint = _fingerprint(bumps)

    # Idempotency: an OPEN PR with this fingerprint already exists → return it.
    existing = (
        await session.execute(
            select(RemediationPullRequest)
            .where(RemediationPullRequest.project_id == project_id)
            .where(RemediationPullRequest.change_fingerprint == fingerprint)
            .where(RemediationPullRequest.status == "open")
            .order_by(RemediationPullRequest.created_at.desc())
        )
    ).scalars().first()
    if existing is not None:
        log.info(
            "remediation_pr.idempotent_hit",
            project_id=str(project_id),
            remediation_pr_id=str(existing.id),
        )
        return RemediationPRResult(record=existing, created=False)

    # Re-validate the STORED repo before any URL interpolation (path-injection
    # guard) and build the branch name from the hex fingerprint we control.
    owner, repo = _validate_repo_full_name(opt_in.repository_full_name)
    head_branch = f"trustedoss/remediation-{fingerprint[:8]}"
    _validate_branch_name(head_branch)

    # Persist a 'creating' row BEFORE the writes so a crash mid-flight leaves a
    # visible trail (no silent partial). Bind the audit team for the commit.
    bind_audit_team(project.team_id)
    record = RemediationPullRequest(
        project_id=project_id,
        installation_row_id=opt_in.installation_row_id,
        ecosystem="npm",
        repository_full_name=opt_in.repository_full_name,
        head_branch=head_branch,
        base_branch="",  # filled once we read the default branch / base ref
        status="creating",
        package_changes=_package_changes_json(bumps),
        change_fingerprint=fingerprint,
        created_by_user_id=actor.id,
    )
    session.add(record)
    try:
        await session.commit()
    except IntegrityError:
        # Lost the lock race: a concurrent request already inserted a 'creating'
        # (or flipped to 'open') row for this fingerprint, and the partial unique
        # index (covering creating|open) rejected our INSERT. Re-read and return
        # the winner idempotently — crucially BEFORE any GitHub work, so two
        # racing requests never open two real PRs.
        await session.rollback()
        winner = (
            await session.execute(
                select(RemediationPullRequest)
                .where(RemediationPullRequest.project_id == project_id)
                .where(RemediationPullRequest.change_fingerprint == fingerprint)
                .where(RemediationPullRequest.status.in_(("creating", "open")))
                .order_by(RemediationPullRequest.created_at.desc())
            )
        ).scalars().first()
        if winner is not None:
            log.info(
                "remediation_pr.lock_race_idempotent_hit",
                project_id=str(project_id),
                remediation_pr_id=str(winner.id),
            )
            return RemediationPRResult(record=winner, created=False)
        raise
    await session.refresh(record)

    base_branch = (project.default_branch or "main").strip() or "main"

    try:
        token_info = await _mint_token(session, opt_in, http_client=http_client)
        token = token_info["token"]
        timeout = github_app_token_http_timeout_seconds()
        owns_client = http_client is None
        client = http_client or httpx.AsyncClient(timeout=timeout)
        try:
            base_sha = await _github_get_base_ref_sha(
                client,
                token=token,
                owner=owner,
                repo=repo,
                base_branch=base_branch,
                timeout=timeout,
            )
            await _github_create_branch(
                client,
                token=token,
                owner=owner,
                repo=repo,
                head_branch=head_branch,
                base_sha=base_sha,
                timeout=timeout,
            )
            file_sha = await _github_get_file_sha(
                client,
                token=token,
                owner=owner,
                repo=repo,
                path=_NPM_MANIFEST_PATH,
                ref=base_branch,
                timeout=timeout,
            )
            await _github_put_manifest(
                client,
                token=token,
                owner=owner,
                repo=repo,
                path=_NPM_MANIFEST_PATH,
                branch=head_branch,
                content_text=dry_run.edited_manifest,
                file_sha=file_sha,
                commit_message=_commit_message(bumps),
                timeout=timeout,
            )
            pr_number, pr_url = await _github_open_pr(
                client,
                token=token,
                owner=owner,
                repo=repo,
                head_branch=head_branch,
                base_branch=base_branch,
                title=_pr_title(bumps),
                body_text=_pr_body(bumps, dry_run),
                timeout=timeout,
            )
        finally:
            if owns_client:
                await client.aclose()
            # Drop the token reference promptly — it must not linger.
            del token
    except RemediationError:
        # A GitHub write (or token mint) failed: flip the row to 'failed' so the
        # attempt is auditable, then re-raise the typed error for the router.
        bind_audit_team(project.team_id)
        record.status = "failed"
        record.base_branch = base_branch
        await session.commit()
        await session.refresh(record)
        raise

    # Success: record the PR coordinates and flip to 'open'.
    bind_audit_team(project.team_id)
    record.status = "open"
    record.base_branch = base_branch
    record.pr_number = pr_number
    record.pr_url = pr_url
    await session.commit()
    await session.refresh(record)

    log.info(
        "remediation_pr.opened",
        project_id=str(project_id),
        remediation_pr_id=str(record.id),
        repository_full_name=opt_in.repository_full_name,
        head_branch=head_branch,
        base_branch=base_branch,
        pr_number=pr_number,
        bump_count=len(bumps),
    )
    return RemediationPRResult(record=record, created=True)


async def _mint_token(
    session: AsyncSession,
    opt_in: _OptIn,
    *,
    http_client: httpx.AsyncClient | None,
) -> dict[str, str]:
    """Mint the installation token via b1, mapping its errors to b3 domain errors.

    b1's ``mint_installation_token`` raises ``GitHubAppError`` subclasses; we wrap
    them in :class:`GitHubWriteError` so the b3 router maps a single error family.
    The token is returned but NEVER logged here (b1 already logs only its expiry).
    """
    from services.github_app_service import (
        GitHubAppError,
        mint_installation_token,
    )

    try:
        result = await mint_installation_token(
            session,
            opt_in.credential_id,
            opt_in.installation_id,
            http_client=http_client,
        )
    except GitHubAppError as exc:
        # Status-only context; b1 already logged the specifics without secrets.
        log.warning("remediation_pr.token_mint_failed", credential_id=str(opt_in.credential_id))
        raise GitHubWriteError(
            "could not mint a GitHub App installation token for the opted-in repo"
        ) from exc
    return {"token": str(result["token"]), "expires_at": str(result.get("expires_at", ""))}


def _commit_message(bumps: list[tuple[str, str | None, str]]) -> str:
    if len(bumps) == 1:
        pkg, _frm, to = bumps[0]
        return f"chore(deps): bump {pkg} to {to} (TrustedOSS remediation)"
    return f"chore(deps): bump {len(bumps)} vulnerable dependencies (TrustedOSS remediation)"


def _pr_title(bumps: list[tuple[str, str | None, str]]) -> str:
    if len(bumps) == 1:
        pkg, _frm, to = bumps[0]
        return f"[TrustedOSS] Bump {pkg} to {to}"
    return f"[TrustedOSS] Bump {len(bumps)} vulnerable npm dependencies"


def _pr_body(bumps: list[tuple[str, str | None, str]], dry_run: DryRunResult) -> str:
    """Human-readable PR description. NEVER includes secrets — bumps only."""
    lines = [
        "This pull request was opened automatically by **TrustedOSS** to "
        "remediate known vulnerabilities in your npm dependencies.",
        "",
        "### Dependency bumps",
        "",
        "| Package | From | To |",
        "| --- | --- | --- |",
    ]
    for pkg, frm, to in bumps:
        lines.append(f"| `{pkg}` | {frm or '?'} | {to} |")
    lines.append("")
    if any(w.code == "lockfile_regeneration_required" for w in dry_run.warnings):
        lines.append(
            "> :warning: **Lockfile not updated.** Only `package.json` was edited. "
            "Run `npm install` to regenerate `package-lock.json` before merging."
        )
    lines.append("")
    lines.append("_Review the changes carefully before merging._")
    return "\n".join(lines)


async def list_remediation_prs(
    session: AsyncSession,
    actor: CurrentUser,
    project_id: uuid.UUID,
    *,
    page: int = 1,
    page_size: int = 50,
) -> tuple[list[RemediationPullRequest], int]:
    """List a project's remediation-PR records (newest first).

    RBAC: any team member (read). Existence-hide on no access (404). Mirrors the
    pagination shape of ``github_app_service.list_installations``.
    """
    project = (
        await session.execute(select(Project).where(Project.id == project_id))
    ).scalar_one_or_none()
    if project is None or not _is_team_member(actor, project.team_id):
        raise ProjectNotAccessible(f"project {project_id} not found")

    page = max(page, 1)
    page_size = max(min(page_size, 200), 1)

    from sqlalchemy import func

    base = select(RemediationPullRequest).where(
        RemediationPullRequest.project_id == project_id
    )
    count_base = (
        select(func.count())
        .select_from(RemediationPullRequest)
        .where(RemediationPullRequest.project_id == project_id)
    )
    total = int((await session.execute(count_base)).scalar_one())
    rows_stmt = (
        base.order_by(
            RemediationPullRequest.created_at.desc(),
            RemediationPullRequest.id.desc(),
        )
        .limit(page_size)
        .offset((page - 1) * page_size)
    )
    rows = list((await session.execute(rows_stmt)).scalars().all())
    return rows, total


