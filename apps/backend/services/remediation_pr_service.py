def _is_super_admin(actor: CurrentUser) -> bool:
    return actor.is_superuser or actor.role == "super_admin"


def _is_team_admin(actor: CurrentUser, team_id: uuid.UUID) -> bool:
    if _is_super_admin(actor):
        return True
    return actor.team_roles.get(team_id) == "team_admin"


def _is_team_member(actor: CurrentUser, team_id: uuid.UUID) -> bool:
    if _is_super_admin(actor):
        return True
    return team_id in actor.team_ids


def _applied_bumps(result: DryRunResult) -> list[tuple[str, str | None, str]]:
    """Extract the bumps that ACTUALLY changed, as sorted ``(pkg, from, to)``.

    Drives both the persisted ``package_changes`` and the idempotency
    fingerprint. We key off the ``changes`` the adapter reported as ``changed``
    (a recommendation whose range already satisfied the target is NOT a bump) and
    join the advisory ``current`` version from the recommendation list.
    """
    current_by_pkg = {rec.package: rec.current_version for rec in result.recommendations}
    target_by_pkg = {rec.package: rec.recommended_version for rec in result.recommendations}
    bumps: list[tuple[str, str | None, str]] = []
    for change in result.changes:
        if not change.changed:
            continue
        target = target_by_pkg.get(change.package)
        if target is None:
            # The change came from a recommendation; if we cannot resolve a
            # target version, skip it rather than fabricate one.
            continue
        bumps.append((change.package, current_by_pkg.get(change.package), target))
    # Sort by package name for a STABLE fingerprint regardless of dict ordering.
    bumps.sort(key=lambda b: b[0])
    return bumps


def _fingerprint(bumps: list[tuple[str, str | None, str]]) -> str:
    """Stable hex digest of the ``(package, to)`` bump set.

    Only ``package`` + ``to`` (target) participate — the advisory ``from``
    version does not change the semantic edit, so two requests that bump the same
    packages to the same targets share a fingerprint (idempotent) even if the
    scan-observed current version drifted.
    """
    canonical = "\n".join(f"{pkg}@{to}" for pkg, _from, to in bumps)
    return hashlib.sha256(canonical.encode("utf-8")).hexdigest()


def _package_changes_json(
    bumps: list[tuple[str, str | None, str]],
) -> list[dict[str, str | None]]:
    """Project the bump set to the JSONB shape stored on the row."""
    return [{"package": pkg, "from": frm, "to": to} for pkg, frm, to in bumps]


def _validate_repo_full_name(repository_full_name: str) -> tuple[str, str]:
    """Re-validate a stored ``owner/repo`` and return ``(owner, repo)``.

    Raises :class:`RemediationConfigError` (422) on a malformed value. Called
    BEFORE the value is interpolated into a GitHub URL so a corrupted / crafted
    row can never smuggle a traversal / CRLF / scheme into the request URL.
    """
    if not isinstance(repository_full_name, str) or not _REPO_FULL_NAME_RE.match(
        repository_full_name
    ):
        raise RemediationConfigError(
            "the opted-in installation's repository_full_name is not a valid "
            "'owner/repo' identifier"
        )
    owner, repo = repository_full_name.split("/", 1)
    return owner, repo


def _validate_branch_name(branch: str) -> None:
    """Belt-and-braces check on a ref name we build before interpolating it."""
    if not _BRANCH_NAME_RE.match(branch):  # pragma: no cover — fingerprint is hex
        raise RemediationConfigError("computed branch name is not a safe git ref")


