@dataclass(frozen=True)
class RemediationPRResult:
    """The outcome of a create-PR request.

    ``record`` is the persisted row (``None`` only when there was nothing to do —
    no recommendations / no manifest change). ``created`` is ``True`` when a NEW
    PR was opened, ``False`` when an existing open PR was returned (idempotent
    hit) or there was nothing to do. ``no_op_reason`` explains a ``None`` record.
    """

    record: RemediationPullRequest | None
    created: bool
    no_op_reason: str | None = None


