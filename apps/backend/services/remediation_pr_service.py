"""
Remediation pull-request service — v2.2 2.2-b3 (opt-in automated npm PR).

This is the b-track capstone. Unlike b2 (``services.remediation_service``),
which computes a side-effect-free dry-run, b3 actually WRITES to a customer's
GitHub repository: it creates a branch, commits the edited ``package.json``, and
opens a pull request, using a short-lived GitHub App installation token minted
by b1 (``services.github_app_service.mint_installation_token``).

Because that is a privileged external write, this module is defensive by design.

Security model (read this before touching the GitHub write path):

  1. **Opt-in is the only authority over the target repo.** The repository is
     NEVER chosen by the caller. ``create_npm_remediation_pr`` resolves the
     project's opted-in ``GitHubAppInstallation`` row (the ``project_id`` link, a
     NON-revoked credential, and a set ``repository_full_name``) and derives the
     target repo + installation token from THAT row only. If the project is not
     opted in (no such installation), the request is REFUSED (409) — we never
     open a PR on a repo the project is not explicitly linked to. There is no
     request field that lets a caller name a repo.

  2. **RBAC: team_admin of the project's team.** Opening a PR is a write to an
     external system, so it requires team_admin (not just developer). Existence-
     hide: a caller who cannot see the project gets the same 404 as a missing
     one (no cross-team enumeration). The read endpoint (``list_remediation_prs``)
     is any team member.

  3. **Re-validate everything at the trust boundary.** Even though
     ``repository_full_name`` is stored, it is re-validated to the exact
     ``owner/repo`` shape before it is interpolated into a GitHub URL
     (path-injection guard). The created branch name is built from a hex
     fingerprint we control (never from untrusted input) and re-validated to a
     safe ref charset. The manifest path is fixed (``package.json``) — the
     commit is scoped to that single file.

  4. **The installation token / App JWT NEVER touch a log or the response.** We
     log ids, repo, branch, and GitHub HTTP STATUS codes only — never response
     bodies (they may carry rate-limit / account detail) and never the token.

  5. **No network in tests.** Every GitHub call goes through an injectable
     ``http_client`` with ``follow_redirects=False`` and a bounded timeout. A
     redirect from GitHub is treated as an error (the token must never follow a
     3xx to an attacker-controlled host).

  6. **Idempotency.** The bump set is hashed to a stable ``change_fingerprint``;
     an existing ``open`` PR with the same fingerprint short-circuits and is
     returned instead of opening a duplicate. A partial unique index backstops
     a concurrent race.

CLAUDE.md compliance: core rule #2 (this ships migration 0021 + the model),
#11 (``os.getenv`` at call time via the config accessors), #12 (the router
requires JWT; the service re-asserts team_admin), §4 (typed domain exceptions →
RFC 7807), §5 (structlog JSON, never secrets).
"""

