# The single manifest file the commit is scoped to. b3 edits ONLY package.json
# (the lockfile is regenerated by the developer via `npm install` — surfaced as a
# warning). Keeping this fixed means the commit can never touch another path.
_NPM_MANIFEST_PATH = "package.json"

# A GitHub "owner/repo" full name. Each segment is the GitHub-allowed identifier
# charset (alnum, hyphen, underscore, dot) — re-validated at the trust boundary
# before URL interpolation even though it was stored, so a corrupted/crafted row
# can never smuggle a path-traversal or CRLF into the GitHub URL.
_REPO_FULL_NAME_RE = re.compile(r"^[A-Za-z0-9._-]{1,100}/[A-Za-z0-9._-]{1,100}$")

# A git ref segment we build ourselves (constant prefix + hex fingerprint). The
# fingerprint is hex so this always matches; re-validating is a belt for the
# "never interpolate an unvalidated value into a URL/ref" rule.
_BRANCH_NAME_RE = re.compile(r"^[A-Za-z0-9._/-]{1,200}$")

_GITHUB_ACCEPT = "application/vnd.github+json"
_GITHUB_API_VERSION = "2022-11-28"


