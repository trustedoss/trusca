async def _github_get_base_ref_sha(
    client: httpx.AsyncClient,
    *,
    token: str,
    owner: str,
    repo: str,
    base_branch: str,
    timeout: float,
) -> str:
    """GET the base branch ref and return its commit SHA."""
    url = f"{github_api_url()}/repos/{owner}/{repo}/git/ref/heads/{base_branch}"
    resp = await _github_request(
        client, "GET", url, token=token, timeout=timeout, step="get_base_ref"
    )
    payload = resp.json()
    try:
        return str(payload["object"]["sha"])
    except (KeyError, TypeError) as exc:
        raise GitHubWriteError("GitHub returned an unexpected base-ref shape") from exc


async def _github_create_branch(
    client: httpx.AsyncClient,
    *,
    token: str,
    owner: str,
    repo: str,
    head_branch: str,
    base_sha: str,
    timeout: float,
) -> None:
    """Create the head branch pointing at ``base_sha``."""
    url = f"{github_api_url()}/repos/{owner}/{repo}/git/refs"
    body = {"ref": f"refs/heads/{head_branch}", "sha": base_sha}
    await _github_request(
        client, "POST", url, token=token, json=body, timeout=timeout, step="create_branch"
    )


async def _github_get_file_sha(
    client: httpx.AsyncClient,
    *,
    token: str,
    owner: str,
    repo: str,
    path: str,
    ref: str,
    timeout: float,
) -> str | None:
    """Return the blob SHA of ``path`` on ``ref``, or ``None`` if absent.

    The PUT contents API needs the existing blob SHA to UPDATE a file (omitting
    it CREATEs). A 404 here means the file does not yet exist on that branch.
    """
    url = f"{github_api_url()}/repos/{owner}/{repo}/contents/{path}?ref={ref}"
    resp = await _github_request(
        client,
        "GET",
        url,
        token=token,
        timeout=timeout,
        step="get_file_sha",
        allow_404=True,
    )
    if resp.status_code == 404:
        return None
    payload = resp.json()
    sha = payload.get("sha") if isinstance(payload, dict) else None
    return str(sha) if sha is not None else None


async def _github_put_manifest(
    client: httpx.AsyncClient,
    *,
    token: str,
    owner: str,
    repo: str,
    path: str,
    branch: str,
    content_text: str,
    file_sha: str | None,
    commit_message: str,
    timeout: float,
) -> None:
    """Commit the edited manifest to ``branch`` (scoped to ``path`` only)."""
    url = f"{github_api_url()}/repos/{owner}/{repo}/contents/{path}"
    encoded = base64.b64encode(content_text.encode("utf-8")).decode("ascii")
    body: dict[str, str] = {
        "message": commit_message,
        "content": encoded,
        "branch": branch,
    }
    if file_sha is not None:
        body["sha"] = file_sha
    await _github_request(
        client, "PUT", url, token=token, json=body, timeout=timeout, step="put_manifest"
    )


async def _github_open_pr(
    client: httpx.AsyncClient,
    *,
    token: str,
    owner: str,
    repo: str,
    head_branch: str,
    base_branch: str,
    title: str,
    body_text: str,
    timeout: float,
) -> tuple[int, str]:
    """Open the PR and return ``(number, html_url)``."""
    url = f"{github_api_url()}/repos/{owner}/{repo}/pulls"
    body = {"title": title, "head": head_branch, "base": base_branch, "body": body_text}
    resp = await _github_request(
        client, "POST", url, token=token, json=body, timeout=timeout, step="open_pr"
    )
    payload = resp.json()
    try:
        return int(payload["number"]), str(payload["html_url"])
    except (KeyError, TypeError, ValueError) as exc:
        raise GitHubWriteError("GitHub returned an unexpected pull-request shape") from exc


async def _github_request(
    client: httpx.AsyncClient,
    method: str,
    url: str,
    *,
    token: str,
    timeout: float,
    step: str,
    json: dict[str, str] | None = None,
    allow_404: bool = False,
) -> httpx.Response:
    """Issue one GitHub request defensively.

    Contract: ``follow_redirects=False`` (the installation token must never
    follow a 3xx to an attacker-controlled host); status-only logging (never the
    response body or the token); a non-2xx (other than an allowed 404) raises
    :class:`GitHubWriteError` carrying the STEP + STATUS only.
    """
    headers = {
        "Authorization": f"Bearer {token}",
        "Accept": _GITHUB_ACCEPT,
        "X-GitHub-Api-Version": _GITHUB_API_VERSION,
    }
    try:
        resp = await client.request(
            method,
            url,
            headers=headers,
            json=json,
            timeout=timeout,
            follow_redirects=False,
        )
    except httpx.HTTPError as exc:
        log.warning("remediation_pr.github_unreachable", step=step)
        raise GitHubWriteError(f"could not reach GitHub during '{step}'") from exc

    if allow_404 and resp.status_code == 404:
        return resp

    # A redirect must NOT be followed (we disabled it) and is not a success — the
    # token could otherwise be replayed against the redirect target.
    if resp.is_redirect or resp.status_code not in (200, 201):
        log.warning(
            "remediation_pr.github_write_non_2xx",
            step=step,
            status_code=resp.status_code,
        )
        raise GitHubWriteError(
            f"GitHub returned status {resp.status_code} during '{step}'"
        )
    return resp


